                        <p class="buff-and-text__text buff-and-text__text_1">Пневмобаллоны (варианты названий – пневмоподушки, пневмохэлперы) – это баллоны из силовой резины толщиной 1,1 см, накаченные воздухом. Они вставляются в полые пружины амортизатора на заднюю ось для усиления штатной подвески (на моделях, не имеющих стойки внутри пружины). Эти устройства предназначены для поднятия клиренса, улучшения амортизации, а также повышения грузоподъемности. </p>
